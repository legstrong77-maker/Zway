/**
 * 分析引擎核心方法：結合星曜屬性與宮位情境，產生一段解讀文。
 */
export function analyzePalaceDynamics(palaceName, majorStars, minorStars) {
  let result = "";
  const domain = palaceDomains[palaceName] || "這個領域";
  const majorNames = majorStars.map(s => s.name);
  const minorNames = minorStars.map(s => s.name);

  // 1. 處理無主星情況
  if (majorNames.length === 0) {
    result += `<p>此宮無主星，通常表示在「${domain}」方面，您容易受到外界環境或是對面宮位（對宮）的深刻影響，缺乏強烈預設的自我主見，但也意味著具有極大的彈性與可塑性。</p>`;
  }
  
  // 2. 處理雙星或單星
  if (majorNames.length === 2) {
    const combo1 = majorNames[0] + majorNames[1];
    const combo2 = majorNames[1] + majorNames[0];
    const comboKey = dualStarMeanings[combo1] ? combo1 : (dualStarMeanings[combo2] ? combo2 : null);
    
    if (comboKey) {
      result += `<div class="analysis-block style="border-left-color: #dc2626;">`;
      result += `<h3><span class="evidence-badge" style="background:#fecaca;color:#b91c1c;">雙星共振</span> ${comboKey}</h3>`;
      result += `<p>當這兩顆星同時落在 ${palaceName} 時，它會強烈影響您的${domain}：</p>`;
      result += `<p><b>${dualStarMeanings[comboKey]}</b></p>`;
      result += `</div>`;
    } else {
      // 未知雙星組合
      result += `<p>在「${domain}」上，受到 ${majorNames[0]} 與 ${majorNames[1]} 的雙重特質影響，呈現混合多元的狀態。</p>`;
    }
  } else if (majorNames.length === 1) {
    // 單星情境化解讀
    const starName = majorNames[0];
    const trait = starCoreTraits[starName] || "獨特";
    result += `<p>在探討您的「${domain}」時，主導的能量是 <b>${starName}</b>。這代表您在此領域會展現出「${trait}」的強烈傾向。`;
    
    // 一些特定星系的專屬造句
    if (starName === "紫微") result += ` 處理相關事務時，您喜歡握有主導權，不喜歡被輕易干涉。`;
    if (starName === "武曲") result += ` 您會以非常務實、甚至講求實質回報的態度來面對，執行力極佳。`;
    if (starName === "天機") result += ` 您會投入大量的腦力與心思去籌劃，但也容易因為想太多而游移不定。`;
    if (starName === "天同") result += ` 您偏好以和諧、輕鬆的方式去應對，不喜歡過於緊繃或壓力的狀態。`;
    if (starName === "七殺" || starName === "破軍") result += ` 您不畏懼改變，敢於在此領域大破大立，但也時常經歷起伏與挑戰。`;
    result += `</p>`;
  }

  // 3. 處理輔助星曜的催化作用
  if (minorNames.length > 0) {
    const goodBadDesc = minorNames.map(m => {
      const tip = minorStarMeanings[m] || "";
      const isBad = ["擎羊", "陀羅", "火星", "鈴星", "地空", "地劫"].includes(m);
      return `<li style="margin-bottom: 4px;"><b>${m}</b>：<span style="color: ${isBad ? '#dc2626' : '#0369a1'}">${tip}</span></li>`;
    }).join('');

    result += `<div style="margin-top: 12px; padding: 12px; background: #e0f2fe; border-radius: 8px;">`;
    result += `<h4 style="color: #0c4a6e; margin-bottom: 8px; font-size: 1rem;">✦ 輔助星曜的環境引化</h4>`;
    result += `<p style="font-size: 0.95rem; margin-bottom: 8px; color: #334155;">除了主星的本質，下列輔星的進駐也為您這個宮位帶來了額外的變數：</p>`;
    result += `<ul style="font-size: 0.95rem; color: #334155; padding-left: 20px;">${goodBadDesc}</ul>`;
    result += `</div>`;
  }

  // 4. 收尾建議
  if (majorNames.length > 0) {
    result += `<p style="margin-top: 12px; color: #64748b; font-size: 0.9rem;">➤ <b>行動建議：</b> 順應 ${majorNames.join('、')} 的特質去發揮優勢；若有遇到煞星，則須在此領域特別留意情緒控管與風險評估，避免盲目衝動。</p>`;
  } else {
    result += `<p style="margin-top: 12px; color: #64748b; font-size: 0.9rem;">➤ <b>行動建議：</b> 本宮為空，建議參考對宮的主星特質來作為發展依據。</p>`;
  }

  return result;
}
